# Задача 5. Игра «Компьютер угадывает число» Мальчик загадывает число между 1 и 100 (включительно).
# Компьютер может спросить у мальчика: «Твоё число равно, меньше или больше, чем число N?», где N —число,
# которое хочет проверить компьютер. Мальчик отвечает одним из трёх чисел: 1 — равно, 2 — больше, 3 — меньше.
# Напишите программу, которая с помощью цепочки таких вопросов и ответов мальчика угадывает число.
# Дополнительно: сделайте так, чтобы можно было гарантированно угадать число за семь попыток.
start = 1
finish = 100
count = 0

print("Загадайте число от 1 до 100 (включительно).")
print("Отвечайте на вопросы: 1 — равно, 2 — больше, 3 — меньше.")

while True:
    n = (start + finish) // 2
    count += 1
    answer = int(input(f"Попытка {count}: Твоё число равно, больше или меньше, чем {n}? (1/2/3): "))

    if answer == 1:
        print(f"Ура! Число угадано за {count} попыток.")
        break
    elif answer == 2:
        start = n + 1
    elif answer == 3:
        finish = n - 1
    else:
        print("Некорректный ответ. Пожалуйста, введите 1, 2 или 3.")
        count -= 1  # Не засчитываем некорректную попытку

    if start > finish:
        print("Кажется, вы где-то ошиблись в ответах. Невозможно угадать число с такими ответами.")
        break


# Загадайте число от 1 до 100 (включительно).
